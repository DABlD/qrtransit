        $data->destination_id = $req->destination_id;
        $data->vehicle_id = $req->vehicle_id;
        $data->user_id = $req->user_id;
        $data->ticket = $req->ticket;
        $data->amount = $req->amount;
        $data->status = $req->status;
        $data->embarked_date = $req->embarked_date;

